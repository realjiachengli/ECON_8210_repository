function [lhs, rhs] = static_resid(y, x, params)
T = NaN(8, 1);
lhs = NaN(4, 1);
rhs = NaN(4, 1);
T(1) = y(3)/y(2);
T(2) = .33*exp(y(4))*T(1)^(-.67);
T(3) = T(2)+.9;
T(4) = 1/y(1)*.97;
T(5) = T(1)^.33;
T(6) = exp(y(4))*1/y(1)*.67;
T(7) = exp(y(4))*y(3)^.33;
T(8) = y(2)^.67;
lhs(1) = y(4);
rhs(1) = y(4)*.95+.007*x(1);
lhs(2) = 1/y(1);
rhs(2) = T(3)*T(4);
lhs(3) = y(2);
rhs(3) = T(5)*T(6);
lhs(4) = y(3);
rhs(4) = T(7)*T(8)+y(3)*.9-y(1);
end
